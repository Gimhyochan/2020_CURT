//*********************************************************************************************************************************
// o SEED_128RoundKey_Generation()	: SEED-128 키스케쥴 함수에서 특정 라운드 키를 생성하는 함수		
// o 입력							: A, B, C, D - 특정 라운드 키를 생성하기 위한 키 스케줄의 내부 상태값
//  								: R	- 해당 라운드 수
// o 출력							: RK	- 특정 라운드 키를 출력하는 변수
//*********************************************************************************************************************************
void SEED_128RoundKey_Generation(ULONG A, ULONG B, ULONG C, ULONG D, ULONG R, ULONG RK[][2])
{
	ULONG T0, T1;

	T0 = A + C - SEED_KC[R];
	T1 = B - D + SEED_KC[R];
	RK[R][0] = T0;
	RK[R][1] = T1;
	SEED_G(RK[R]);
	SEED_G(RK[R] + 1);
}

//*********************************************************************************************************************************
// o SEED_192RoundKey_Generation()	: SEED-192 키스케쥴 함수에서 특정 라운드 키를 생성하는 함수		
// o 입력							: A, B, C, D, E, F - 특정 라운드 키를 생성하기 위한 키 스케줄의 내부 상태값
//  								: R	- 해당 라운드 수
// o 출력							: RK	- 특정 라운드 키를 출력하는 변수
//*********************************************************************************************************************************
void SEED_192RoundKey_Generation(ULONG A, ULONG B, ULONG C, ULONG D, ULONG E, ULONG F, ULONG R, ULONG RK[][2])
{
	ULONG T0, T1;

	T0 = ((A + C) ^ D) - SEED_KC[R];
	T1 = ((B - E) ^ F) + SEED_KC[R];
	RK[R][0] = T0;
	RK[R][1] = T1;
	SEED_G(RK[R]);
	SEED_G(RK[R] + 1);
}

//*********************************************************************************************************************************
// o SEED_256RoundKey_Generation()	: SEED-256 키스케쥴 함수에서 특정 라운드 키를 생성하는 함수		
// o 입력							: A, B, C, D, E, F, G, H - 특정 라운드 키를 생성하기 위한 키 스케줄의 내부 상태값
//  								: R	- 해당 라운드 수
// o 출력							: RK	- 특정 라운드 키를 출력하는 변수
//*********************************************************************************************************************************
void SEED_256RoundKey_Generation(ULONG A, ULONG B, ULONG C, ULONG D, ULONG E, ULONG F, ULONG G, ULONG H, ULONG R, ULONG RK[][2])
{
	ULONG T0, T1;

	T0 = (((A + C) ^ E) - F) ^ SEED_KC[R];
	T1 = (((B - D) ^ G) + H) ^ SEED_KC[R];
	RK[R][0] = T0;
	RK[R][1] = T1;
	SEED_G(RK[R]);
	SEED_G(RK[R] + 1);
}

//*********************************************************************************************************************************
// o TwoWordRRot()				: SEED 키스케쥴 함수에서 사용되는 오른쪽 64비트 로테이션 함수		
// o 입력						: A||B	- 순환이동함수에 입력된 32비트 워드 2개
// o 출력						: A||B	- 수환이동함수에 의하여 갱신된 32 비트 워드 2개
//*********************************************************************************************************************************
#define TwoWordRRot(A,B) 				\
		T = A;							\
		A = (A>>8) ^ (B<<24);			\
		B = (B>>8) ^ (T<<24);			\

//*********************************************************************************************************************************
// o TwoWordLRot()				: SEED 키스케쥴 함수에서 사용되는 왼쪽 64비트 로테이션 함수		
// o 입력						: A||B	- 순환이동함수에 입력된 32비트 워드 2개
// o 출력						: A||B	- 수환이동함수에 의하여 갱신된 32 비트 워드 2개
//*********************************************************************************************************************************
#define TwoWordLRot(A,B) 				\
		T = A;							\
		A = (A<<8) ^ (B>>24);			\
		B = (B<<8) ^ (T>>24);			\
//*********************************************************************************************************************************
// o ThreeWordRRot()				: SEED 키스케쥴 함수에서 사용되는 오른쪽 96비트 로테이션 함수		
// o 입력						: A||B||C	- 순환이동함수에 입력된 32비트 워드 3개
// o 출력						: A||B||C	- 수환이동함수에 의하여 갱신된 32 비트 워드 3개
//*********************************************************************************************************************************
#define ThreeWordRRot(A,B,C,Rot) 		\
		T = A;							\
		A = (A>>Rot) ^ (B<<(32-Rot));	\
		B = (B>>Rot) ^ (C<<(32-Rot));	\
		C = (C>>Rot) ^ (T<<(32-Rot));	\

//*********************************************************************************************************************************
// o ThreeWordLRot()				: SEED 키스케쥴 함수에서 사용되는 왼쪽 96비트 로테이션 함수		
// o 입력						: A||B||C	- 순환이동함수에 입력된 32비트 워드 3개
// o 출력						: A||B||C	- 수환이동함수에 의하여 갱신된 32 비트 워드 3개
//*********************************************************************************************************************************
#define ThreeWordLRot(A,B,C,Rot) 		\
		T = A;							\
		A = (A<<Rot) ^ (B>>(32-Rot));	\
		B = (B<<Rot) ^ (C>>(32-Rot));	\
		C = (C<<Rot) ^ (T>>(32-Rot));	\

//*********************************************************************************************************************************
// o FourWordRRot()				: SEED 키스케쥴 함수에서 사용되는 오른쪽 128비트 로테이션 함수		
// o 입력						: A||B||C||D	- 순환이동함수에 입력된 32비트 워드 4개
// o 출력						: A||B||C||D	- 수환이동함수에 의하여 갱신된 32 비트 워드 4개
//*********************************************************************************************************************************
#define FourWordRRot(A,B,C,D,Rot)		\
		T = A;							\
		A = (A>>Rot) ^ (B<<(32-Rot));	\
		B = (B>>Rot) ^ (C<<(32-Rot));	\
		C = (C>>Rot) ^ (D<<(32-Rot));	\
		D = (D>>Rot) ^ (T<<(32-Rot));	\

//*********************************************************************************************************************************
// o FourWordLRot()				: SEED 키스케쥴 함수에서 사용되는 왼쪽 128비트 로테이션 함수		
// o 입력						: A||B||C||D	- 순환이동함수에 입력된 32비트 워드 4개
// o 출력						: A||B||C||D	- 수환이동함수에 의하여 갱신된 32 비트 워드 4개
//*********************************************************************************************************************************
#define FourWordLRot(A,B,C,D,Rot)		\
		T = A;							\
		A = (A<<Rot) ^ (B>>(32-Rot));	\
		B = (B<<Rot) ^ (C>>(32-Rot));	\
		C = (C<<Rot) ^ (D>>(32-Rot));	\
		D = (D<<Rot) ^ (T>>(32-Rot));	\

//*********************************************************************************************************************************
// o SEED_128KeyGeneration()	: SEED 키스케쥴 함수로서 128 비트 마스터키를 입력 받아 128 비트 라운드 키를 생성하는 함수		
// o 입력						: MK[]		- SEED 알고리즘의 마스터 키를 입력받는 변수 
// o 출력						: RK[][]	- SEED 키 스케줄에 의하여 생성된 라운드 키를 출력하는 함수
//*********************************************************************************************************************************

void SEED_128KeyGeneration(const UCHAR MK[], ULONG RK[][2])
{
	ULONG A, B, C, D;
	ULONG T;
	ULONG i;

	A = u4byte_in(MK);	B = u4byte_in(MK + 4);
	C = u4byte_in(MK + 8);	D = u4byte_in(MK + 12);

	for (i = 0; i < 16; i += 2)
	{
		SEED_128RoundKey_Generation(A, B, C, D, i, RK);
		TwoWordRRot(A, B);
		SEED_128RoundKey_Generation(A, B, C, D, (i + 1), RK);
		TwoWordLRot(C, D);
	}
}

//*********************************************************************************************************************************
// o SEED_192KeyGeneration()	: SEED 키스케쥴 함수로서 128 비트 마스터키를 입력 받아 192 비트 라운드 키를 생성하는 함수		
// o 입력						: MK[]		- SEED 알고리즘의 마스터 키를 입력받는 변수 
// o 출력						: RK[][]	- SEED 키 스케줄에 의하여 생성된 라운드 키를 출력하는 함수
//*********************************************************************************************************************************

void SEED_192KeyGeneration(const UCHAR MK[], ULONG RK[][2])
{
	ULONG A, B, C, D, E, F;
	ULONG T;
	ULONG Rot;
	ULONG R;

	A = u4byte_in(MK);	B = u4byte_in(MK + 4);
	C = u4byte_in(MK + 8);	D = u4byte_in(MK + 12);
	E = u4byte_in(MK + 16);	F = u4byte_in(MK + 20);

	for (R = 0; R < 20; R += 2)
	{
		switch ((R >> 1) % 3) {
		case 0: Rot = 9; break;
		case 1: Rot = 8; break;
		case 2: Rot = 12; break;
		}
		SEED_192RoundKey_Generation(A, B, C, D, E, F, R, RK);
		ThreeWordRRot(A, B, C, Rot);
		SEED_192RoundKey_Generation(A, B, C, D, E, F, (R + 1), RK);
		ThreeWordLRot(D, E, F, Rot);
	}
}

//*********************************************************************************************************************************
// o SEED_256KeyGeneration()	: SEED 키스케쥴 함수로서 128 비트 마스터키를 입력 받아 256 비트 라운드 키를 생성하는 함수		
// o 입력						: MK[]		- SEED 알고리즘의 마스터 키를 입력받는 변수 
// o 출력						: RK[][]	- SEED 키 스케줄에 의하여 생성된 라운드 키를 출력하는 함수
//*********************************************************************************************************************************

void SEED_256KeyGeneration(const UCHAR MK[], ULONG RK[][2])
{
	ULONG A, B, C, D, E, F, G, H;
	ULONG T;
	ULONG Rot;
	ULONG R;

	A = u4byte_in(MK);	B = u4byte_in(MK + 4);
	C = u4byte_in(MK + 8);	D = u4byte_in(MK + 12);
	E = u4byte_in(MK + 16);	F = u4byte_in(MK + 20);
	G = u4byte_in(MK + 24);	H = u4byte_in(MK + 28);


	for (R = 0; R < 24; R += 2)
	{
		switch ((R >> 1) % 3) {
		case 0: Rot = 9; break;
		case 1: Rot = 11; break;
		case 2: Rot = 12; break;
		}
		SEED_256RoundKey_Generation(A, B, C, D, E, F, G, H, R, RK);
		FourWordRRot(A, B, C, D, Rot);
		SEED_256RoundKey_Generation(A, B, C, D, E, F, G, H, (R + 1), RK);
		FourWordLRot(E, F, G, H, Rot);
	}
}

//*********************************************************************************************************************************
// o SEED_KeySchedule()	: SEED 키스케쥴 함수로서 128 비트 마스터키를 입력 받아 라운드 키를 생성하는 함수		
// o 입력				: MK[]		- SEED 알고리즘의 마스터 키를 입력받는 변수 
// o 출력				: RK[][]	- SEED 키 스케줄에 의하여 생성된 라운드 키를 출력하는 함수
//*********************************************************************************************************************************

void SEED_KeySchedule(const UCHAR MK[], ULONG RK[][2], const ULONG KeySize)
{
	if (KeySize == 128)		SEED_128KeyGeneration(MK, RK);
	else if (KeySize == 192) SEED_192KeyGeneration(MK, RK);
	else					SEED_256KeyGeneration(MK, RK);
}

