//*********************************************************************************************************************************
// o u4byte_in()	: 8비트 상수 4개를 32비트 상수 묶음으로 변환
//*********************************************************************************************************************************
u32 u4byte_in(const UCHAR* x) {
	return (x[0] << 24) | (x[1] << 16) | (x[2] << 8) | x[3];
}

//*********************************************************************************************************************************
// o u4byte_out()	: 32비트 상수를 8비트 상수 4개로 나누는데 사용하는 함수
//*********************************************************************************************************************************
void u4byte_out(UCHAR* x, const u32 y) {
	x[0] = (y >> 24) & 0xff;
	x[1] = (y >> 16) & 0xff;
	x[2] = (y >> 8) & 0xff;
	x[3] = y & 0xff;
}

//*********************************************************************************************************************************
// o SEED_G[]	: SEED_Round() 함수와 SEED_KeySchedule() 함수에 사용되는 함수
// o 입력		: S	- 4-바이트 입력 변수
// o 출력		: S	- 4-바이트 입력 변수를 갱신한 뒤 출력하는 변수
//*********************************************************************************************************************************
void SEED_G(ULONG* S)
{
	UCHAR  Y[4];

	Y[0] = SEED_S1box[((*S) >> 0) & 0xFF];
	Y[1] = SEED_S2box[((*S) >> 8) & 0xFF];
	Y[2] = SEED_S1box[((*S) >> 16) & 0xFF];
	Y[3] = SEED_S2box[((*S) >> 24) & 0xFF];

	(*S) = ((ULONG)((Y[0] & 0xFC) ^ (Y[1] & 0xF3) ^ (Y[2] & 0xCF) ^ (Y[3] & 0x3F)) << 0) |
		((ULONG)((Y[0] & 0xF3) ^ (Y[1] & 0xCF) ^ (Y[2] & 0x3F) ^ (Y[3] & 0xFC)) << 8) |
		((ULONG)((Y[0] & 0xCF) ^ (Y[1] & 0x3F) ^ (Y[2] & 0xFC) ^ (Y[3] & 0xF3)) << 16) |
		((ULONG)((Y[0] & 0x3F) ^ (Y[1] & 0xFC) ^ (Y[2] & 0xF3) ^ (Y[3] & 0xCF)) << 24);
}

//*********************************************************************************************************************************
// o SEED_Round[]	: SEED에 사용되는 라운드 함수 
// o 입력			: R[]	- 8-바이트 내부 상태를 입력받는 변수
//					: K[]   - 8-바이트 라운드 키를 입력받는 변수
// o 출력			: L[]	- 8-바이트 내부 상태를 갱신한 뒤 출력하는 변수
//*********************************************************************************************************************************
void SEED_RoundF(ULONG L[2], ULONG R[2], ULONG K[2])
{
	ULONG temp[2];

	temp[0] = R[0] ^ K[0];
	temp[1] = R[1] ^ K[1];

	temp[1] ^= temp[0];

	SEED_G(temp + 1);
	temp[0] += temp[1];

	SEED_G(temp);
	temp[1] += temp[0];

	SEED_G(temp + 1);
	temp[0] += temp[1];

	L[0] ^= temp[0];
	L[1] ^= temp[1];
}

//*********************************************************************************************************************************
// o SEED_IStateTransformation()	: SEED 암호 알고리즘의 평문을 내부상태로 변환하는 함수	
// o 입력		: PT	- SEED 암호 알고리즘에 입력된 평문
// o 출력		: L[]	- SEED 암호 알고리즘의 왼쪽 내부 상태값
//      		: R[]	- SEED 암호 알고리즘의 오른쪽 내부 상태값
//*********************************************************************************************************************************
void SEED_IStateTransformation(const UCHAR PT[16], ULONG L[2], ULONG R[2])
{
	L[0] = u4byte_in(PT);		L[1] = u4byte_in(PT + 4);
	R[0] = u4byte_in(PT + 8);		R[1] = u4byte_in(PT + 12);
}

//*********************************************************************************************************************************
// o SEED_OStateTransformation()	: SEED 암호 알고리즘의 평문을 내부상태로 변환하는 함수	
// o 입력		: L[]	- SEED 암호 알고리즘의 왼쪽 내부 상태값
//      		: R[]	- SEED 암호 알고리즘의 오른쪽 내부 상태값
// o 출력		: CT	- SEED 암호 알고리즘에 출력할 평문
//*********************************************************************************************************************************
void SEED_OStateTransformation(UCHAR CT[16], const ULONG L[2], const ULONG R[2])
{
	u4byte_out(CT, L[0]);		u4byte_out(CT + 4, L[1]);
	u4byte_out(CT + 8, R[0]);		u4byte_out(CT + 12, R[1]);
}


//*********************************************************************************************************************************
// o SEED_ENC()	: SEED 암호화 함수로서 128 비트 (16 바이트)평문과 라운드 키를 입력받아 암호문을 출력하는 함수		
// o 입력		: PT[]	- 암호화되어야 할 128-비트(16-바이트) 평문을 입력받는 변수
//				: RK[]	- 라운드 키를 입력받는 변수 
// o 출력		: CT[]	- 입력된 평문을 암호화한 암호문을 출력하는 변수
//*********************************************************************************************************************************
void SEED_ENC(const UCHAR PT[16], UCHAR CT[16], ULONG RK[][2], ULONG keysize)
{
	ULONG L[2], R[2];	// SEED 암호화 함수에서의 내부 상태 변수
	SCHAR Round;		// 라운드 함수 반복을 위한 현재 상태의 라운드를 가리키는 변수		
	UCHAR Nr;

	Nr = (UCHAR)(8 + keysize / 16);

	// 평문을 내부상태로 변환함
	SEED_IStateTransformation(PT, L, R);

	// 1-16 라운드 함수
	for (Round = 0; Round < Nr; Round += 2)
	{
		SEED_RoundF(L, R, RK[Round]);
		SEED_RoundF(R, L, RK[Round + 1]);
	}

	// 내부상태를 암호문으로 입력함
	SEED_OStateTransformation(CT, R, L);
}


